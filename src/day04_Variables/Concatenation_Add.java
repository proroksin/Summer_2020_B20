package day04_Variables;

public class Concatenation_Add {
    public static void main(String[] args) {
        // adition: number + number
        System.out.println(12+13);// 25
        System.out.println('A'+2);//A+2=67(character->A=65)
        System.out.println('A'+'B');//131

        //Concatenation : append
        System.out.println("12"+13);//1213 "12" - STRING; 13 - NUMBER
        System.out.println("A"+2); // "A" - is string; A2
        System.out.println("Gender:"+'M');
        System.out.println("Tax:"+3.5+'%'); //tax: 3.5
        System.out.println(3.5+'%'+"Tax");
                          //3.5+37+"Tax" = 40.5Tax








    }
}
