package day03_sequences_Variables;

public class Print_VS_Println {

    public static void main(String[] args) {
        System.out.print("A");
        System.out.print("B");
        System.out.print("C");
        System.out.println("        ");

        System.out.println("     ^    ");
        System.out.println("    / \\");
        System.out.println("   /   \\");
        System.out.println("  /     \\");
        System.out.println(" /       \\");
        System.out.println(" ---------");

        System.out.println("     _    ");
        System.out.println("    /*\\");
        System.out.println("   /***\\");
        System.out.println("  /*****\\");
        System.out.println(" /*******\\");
        System.out.println(" ---------");
        System.out.println(" \\*******/");
        System.out.println("  \\*****/");
        System.out.println("   \\***/");
        System.out.println("    \\*/");
        System.out.println("     -");

    }




}
